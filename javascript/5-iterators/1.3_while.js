/**
 * 
 * The while statement creates a loop that executes
 *  a specified statement as long as the test condition evaluates to true. 
 * The condition is evaluated before executing the statement.
 */

/* Syntax

while (condition) {
  statement
}

 */


let n = 1;

while (n < 3) { // condition


  n++ // increment
}

console.log(n)